package rajawali.math;

public final class MatrixUtil {
	public static float getDeterminant(float[] matrix) {
		float n11 = matrix[0], n12 = matrix[1], n13 = matrix[2], n14 = matrix[3],
		n21 = matrix[4], n22 = matrix[5], n23 = matrix[6], n24 = matrix[7],
		n31 = matrix[8], n32 = matrix[9], n33 = matrix[10], n34 = matrix[11],
		n41 = matrix[12], n42 = matrix[13], n43 = matrix[14], n44 = matrix[15];

		return (
			n14 * n23 * n32 * n41-
			n13 * n24 * n32 * n41-
			n14 * n22 * n33 * n41+
			n12 * n24 * n33 * n41+

			n13 * n22 * n34 * n41-
			n12 * n23 * n34 * n41-
			n14 * n23 * n31 * n42+
			n13 * n24 * n31 * n42+

			n14 * n21 * n33 * n42-
			n11 * n24 * n33 * n42-
			n13 * n21 * n34 * n42+
			n11 * n23 * n34 * n42+

			n14 * n22 * n31 * n43-
			n12 * n24 * n31 * n43-
			n14 * n21 * n32 * n43+
			n11 * n24 * n32 * n43+

			n12 * n21 * n34 * n43-
			n11 * n22 * n34 * n43-
			n13 * n22 * n31 * n44+
			n12 * n23 * n31 * n44+

			n13 * n21 * n32 * n44-
			n11 * n23 * n32 * n44-
			n12 * n21 * n33 * n44+
			n11 * n22 * n33 * n44
		);
	}
}
